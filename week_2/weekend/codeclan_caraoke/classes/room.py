class Room:
    def __init__(self, name, capacity):
        self.name = name
        self.capacity = capacity
        self.total_in_room = 0
        self.songs = []



    def add_one_to_room(self):
        total_in_room = 0
        total_in_room += 1
        return total_in_room 

    def add_guest(guest):
        people_in_room = []
        people_in_room.append(guest.name)
        return people_in_room
       

    def add_song_to_room(song):
        songs = []
        songs.append(song.name)
        return songs


